def avg(str):
    sum = 0
    for i in str :
        sum += i
    rounding(sum, (len(str)-1))


def rounding(ret,rer):
    data = ret / rer
    if data > 0 :
        print(round(data),end = ' ')
    elif data < 0:
        data_1 = ret // rer
        data_2 = data - data_1
        if data_2 >0.5:
            print((data_1+1),end = ' ')
        elif data_2 <= 0.5:
            print(data_1,end = ' ')
    else :
        print(0)


